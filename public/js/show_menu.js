function show_menu(){
  document.getElementById('welcome_block').style = 'left:4vw';
  document.getElementById('book_block').style = 'left:4vw';
  document.getElementById('menu_block').style = 'display:block';
}

function hidden_menu(){
    document.getElementById('welcome_block').style = 'left:30vw';
    document.getElementById('book_block').style = 'left:20vw';
    document.getElementById('menu_block').style = 'display:none';
  }
